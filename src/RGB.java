
class RGB {

    private int mRed;
    private int mGreen;
    private int mBlue;
    private int mGBlue;
    private int mGRed;
    private int mBGreen;
    private int mBRed;
	
    public RGB(int mRed, int mGreen, int mBlue) {
		super();
		this.mRed = mRed;
		this.mGreen = mGreen;
		this.mBlue = mBlue;
	}

	public int getmRed() {
		return mRed;
	}

	public void setmRed(int mRed) {
		this.mRed = mRed;
	}

	public int getmGreen() {
		return mGreen;
	}

	public void setmGreen(int mGreen) {
		this.mGreen = mGreen;
	}

	public int getmBlue() {
		return mBlue;
	}

	public void setmBlue(int mBlue) {
		this.mBlue = mBlue;
	}
    
    
}
