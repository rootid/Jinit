class RGB {

    private final int mRed;
    private final int mGreen;
    private final int mBlue;
	
    public RGB(int mRed, int mGreen, int mBlue) {
        super();
	this.mRed = mRed;
	this.mGreen = mGreen;
	this.mBlue = mBlue;
    }

    public int getmRed() {
    	return mRed;
    }
    public int getmGreen() {
    	return mGreen;
    }
    
    public int getmBlue() {
    	return mBlue;
    }
    
    
}
